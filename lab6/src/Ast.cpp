void FuncFParams::genCode()//函数形参列表
{
    Function *func = builder -> getInsertBB() -> getParent();
    for(long unsigned int i = 0; i < FPs.size(); i++)
    {
        BasicBlock *entry = func->getEntry();                       // 找到current基本块
        IdentifierSymbolEntry *se = dynamic_cast<IdentifierSymbolEntry *>(FPs[i]->getSymPtr()); //建立一个对符号表的索引
        Type *type1 = new PointerType(se->getType());               // type1表示指向se中类型的指针
        Type *type2 = new IntType(32);                              // i32
        SymbolEntry *addr_se = new TemporarySymbolEntry(type2, SymbolTable::getLabel()); // i32类型的addr_se
        Operand *addr = new Operand(addr_se);

        SymbolTable :: counter++; //为了分配新的寄存器
        SymbolEntry *addr_se2 = new TemporarySymbolEntry(type1, SymbolTable::getLabel());// 指针类型的addr_se
        Operand *addr2 = new Operand(addr_se2);

        Instruction *alloca;
        alloca = new AllocaInstruction(addr2, se);                   // allocate space for local id in function stack.
        entry->insertBack(alloca);                                   // allocate instructions should be inserted into the begin of the entry block. 
        StoreInstruction *store = new StoreInstruction(addr2, addr); // 把形参放到分配的寄存器里
        entry -> insertBack(store);                                  // store指令添加到head前面

        se->setAddr(addr2);
        func->params.push_back(addr); 
    }
}

