void DeclStmt::genCode()
{

    //std::cout  << "start8" << std::endl;
    for(auto id: ids->Ids)
    {
        IdentifierSymbolEntry *se = dynamic_cast<IdentifierSymbolEntry *>(id-> getSymPtr());
        // global
        if(se->isGlobal())
        {
            Operand *addr; //符号表指针
            SymbolEntry *addr_se = new IdentifierSymbolEntry(*se); //构建符号表索引的指针
            addr_se->setType(new PointerType(se->getType()));  //讲变量类型转换为指针类型
            addr = new Operand(addr_se); //变量地址
            se->setAddr(addr); //设置变量地址
            bool temp = false; //assignment or not 
            Operand *src;
            // int a,b,c = 2,3,4;
            for(long unsigned int i = 0; i < ids -> Assigns.size(); i++)
            {
                if(ids -> Assigns[i] -> lval -> symbolEntry == se)
                {
                    ids -> Assigns[i] -> genCode();
                    src = ids -> Assigns[i] -> expr -> getOperand();
                    temp = true;
                    break;
                }              
            }
            // int a,b,c; 
            if(temp == false)
            {
                SymbolEntry *se = new ConstantSymbolEntry(TypeSystem::intType, 0);
                Constant* digit = new Constant(se);
                src = digit -> getOperand();
            }
            Instruction *alloca = new MultiAllocaInstruction(src, addr, se);
            alloca -> output();
        }
        else if(se->isLocal())
        {
            Function *func = builder->getInsertBB()->getParent();
            BasicBlock *entry = func->getEntry();

            Type *type;
            type = new PointerType(se->getType());
            SymbolEntry *addr_se;
            Operand *addr;
            addr_se = new TemporarySymbolEntry(type, SymbolTable::getLabel());
            addr = new Operand(addr_se);
            Instruction *alloca = new AllocaInstruction(addr, se);                   // allocate space for local id in function stack.
            entry->insertFront(alloca);                                 // allocate instructions should be inserted into the begin of the entry block.
            se->setAddr(addr);                                          // set the addr operand in symbol entry so that we can use it in subsequent code generation.
            
        }
    }
    for(long unsigned int i = 0; i < ids -> Assigns.size(); i++)
    {
        IdentifierSymbolEntry *se = dynamic_cast<IdentifierSymbolEntry *>(ids -> Assigns[i] -> lval -> getSymPtr());
        if(se -> isGlobal())
        { 
            continue;                   
        }
        else if(se -> isLocal()) // (local)int a,b,c=2,3,4;
        {
            Operand *addr = dynamic_cast<IdentifierSymbolEntry*>(ids -> Assigns[i] -> lval ->getSymPtr())->getAddr();
            se->setAddr(addr); 
            ids -> Assigns[i] -> genCode();
        }
    }
    //std::cout  << "end8" << std::endl;
}

