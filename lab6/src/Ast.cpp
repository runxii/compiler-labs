void BinaryExpr::genCode()
{
    BasicBlock *bb = builder->getInsertBB();//找到当前的BB
    Function *func = bb->getParent();
    if (op == AND)
    {
        /* 逻辑与有短路的属性，即
         * 当expr1的属性为false时，整个expr的属性为假，不用执行expr2
         * 当expr1的属性位true时，根据expr2来确定整个expr的属性
         * 
         * 1. 先创建一个基本块trueBB，（expr2 generate的code要insert的位置）
         * 2. then gen expr1的中间代码
         */
        BasicBlock *trueBB = new BasicBlock(func);  // if the result of lhs is true, jump to the trueBB.
        expr1->genCode();
        backPatch(expr1->trueList(), trueBB);
        builder->setInsertBB(trueBB);               // set the insert point to the trueBB so that intructions generated by expr2 will be inserted into it.
        expr2->genCode();
        Operand *src1 = expr1->getOperand();
        Operand *src2 = expr2->getOperand();

        if(strcmp(src1->getType()->toStr().c_str(),src2->getType()->toStr().c_str())==0){
            src1 = expr1->dst;
            src2 = expr2->dst;
        }
        else if(strcmp(src1->getType()->toStr().c_str(),"i1")==0){
            Operand* t =new Operand(new TemporarySymbolEntry(TypeSystem::intType,SymbolTable::getLabel()));
            new ZextInstruction(t,expr1->dst,bb);
            src1=t;
        }
        else if(strcmp(src2->getType()->toStr().c_str(),"i1")==0){
            Operand* s =new Operand(new TemporarySymbolEntry(TypeSystem::intType,SymbolTable::getLabel()));
            new ZextInstruction(s,expr2->dst,bb);
            src2=s;
        }
        true_list = expr2->trueList();
        false_list = merge(expr1->falseList(), expr2->falseList());
        if(dst->getType()->kind!=4){
            dst->getType()->kind=4;
        }
    }
    else if(op == OR)
    {
        //Todo
        /* 逻辑或也有短路的属性，即
         * 当expr1的结果为true，则整个expr的属性为true
         * 当expr1的结果为false，则整个expr的属性取决于expr2的结果
         */
        BasicBlock *falseBB = new BasicBlock(func);
        expr1 -> genCode();
        backPatchFalse(expr1->falseList(), falseBB);
        builder->setInsertBB(falseBB);
        expr2->genCode();
        Operand *src1 = expr1->getOperand();
        Operand *src2 = expr2->getOperand();

        if(strcmp(src1->getType()->toStr().c_str(),src2->getType()->toStr().c_str())==0){
            src1 = expr1->dst;
            src2 = expr2->dst;
        }
        else if(strcmp(src1->getType()->toStr().c_str(),"i1")==0){
            Operand* t =new Operand(new TemporarySymbolEntry(TypeSystem::intType,SymbolTable::getLabel()));
            new ZextInstruction(t,expr1->dst,bb);
            src1=t;
        }
        else if(strcmp(src2->getType()->toStr().c_str(),"i1")==0){
            Operand* s =new Operand(new TemporarySymbolEntry(TypeSystem::intType,SymbolTable::getLabel()));
            new ZextInstruction(s,expr2->dst,bb);
            src2=s;
        }
        false_list=expr2->falseList();
        true_list=merge(expr1->trueList(), expr2->trueList());
        if(dst->getType()->kind!=4){
            dst->getType()->kind=4;
        }
    }
    else if(op >= LESS && op <= MORE)
    {
        // Todo
        expr1->genCode();
        expr2->genCode();
        Operand *src1 = expr1->getOperand();
        Operand *src2 = expr2->getOperand();

        if(strcmp(src1->getType()->toStr().c_str(),src2->getType()->toStr().c_str())==0){
            src1 = expr1->dst;
            src2 = expr2->dst;
        }
        else if(strcmp(src1->getType()->toStr().c_str(),"i1")==0){
            Operand* t =new Operand(new TemporarySymbolEntry(TypeSystem::intType,SymbolTable::getLabel()));
            new ZextInstruction(t,expr1->dst,bb);
            src1=t;
        }
        else if(strcmp(src2->getType()->toStr().c_str(),"i1")==0){
            Operand* s =new Operand(new TemporarySymbolEntry(TypeSystem::intType,SymbolTable::getLabel()));
            new ZextInstruction(s,expr2->dst,bb);
            src2=s;
        }

        int opcode;
        switch (op)
        {
        case MORE:
            opcode = CmpInstruction::G;
            break;
        case MOREEQUAL:
            opcode = CmpInstruction::GE;
            break;
        case LESS:
            opcode = CmpInstruction::L;
            break;
        case LESSEQUAL:
            opcode = CmpInstruction::LE;
            break;
        case EQUAL:
            opcode = CmpInstruction::E;
            break;
        case NOEQUAL:
            opcode = CmpInstruction::NE;
            break;
        default:
            break;
        }

        new CmpInstruction(opcode, dst, src1, src2, bb);

        //自行添加的正确错误列表合并
        true_list = merge(expr1->trueList(), expr2->trueList());
        false_list = merge(expr1->falseList(), expr2->falseList());
        Instruction* temp = new CondBrInstruction(nullptr,nullptr,dst,bb);
        this->trueList().push_back(temp);
        this->falseList().push_back(temp);
        if(dst->getType()->kind!=4){
            //std::cout<<" ## dst ## " << dst->toStr()<< "  " <<dst->getType()->toStr()<<std::endl;
            // if(strcmp(dst->getType()->toStr().c_str(),"i32")==0){
            //     std::cout<< " opcode " << op <<" ## dst ## " << dst->toStr()<< "  " <<dst->getType()->toStr()<<std::endl;
            //     Operand* p =new Operand(new TemporarySymbolEntry(TypeSystem::boolType,SymbolTable::getLabel()));
            //     new TruncInstruction(p,dst,bb);
            //     dst=p;
            // }
            dst -> getType() -> kind = 4;
        }
    }
    else if(op >= ADD && op <= SUB)
    {
        expr1->genCode();
        expr2->genCode();
        Operand *src1 = expr1->getOperand();
        Operand *src2 = expr2->getOperand();
        int opcode;
        switch (op)
        {
        case ADD:
            opcode = BinaryInstruction::ADD;
            break;
        case SUB:
            opcode = BinaryInstruction::SUB;
            break;
        case MUL:
            opcode = BinaryInstruction::MUL;
            break;
        case DIV:
            opcode = BinaryInstruction::DIV;
            break;
        case PERC:
            opcode = BinaryInstruction::MOD;
            break;
        }
        new BinaryInstruction(opcode, dst, src1, src2, bb);
    }
}

